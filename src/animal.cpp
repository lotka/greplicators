#include "animal.h"
#include "tools.h"

////////////////////////////////////////////////////////////////////////////////////////
// constructor
////////////////////////////////////////////////////////////////////////////////////////
animal::animal()
{
    type    = 1;
    energy  = 100;
    hp      = 100;
}

////////////////////////////////////////////////////////////////////////////////////////
// update
////////////////////////////////////////////////////////////////////////////////////////
void animal::update()
{
    --energy;

    ++age;

    if(energy <= 0 )
    {
       hp = 0;
       std::cout << "Animal died of starvation" << std::endl;
    }
}

////////////////////////////////////////////////////////////////////////////////////////
// update
////////////////////////////////////////////////////////////////////////////////////////
void animal::update(entitySet collidingEntitySet)
{
  //Do updates which don't depend on the situation
  update();

  if(collidingEntitySet.size() != 0)
  {
    for(entitySet::iterator it = collidingEntitySet.begin(); it != collidingEntitySet.end(); ++it)
    {
      int type = (*it)->getType();
      switch(type)
      {
         case 1:
         (*it)->fight();
         case 2:
	 (*it)->eat();
      }      
    }
  }
  else
  {
     currentPosition.moveRandom();
  }

}

////////////////////////////////////////////////////////////////////////////////////////
// fight
////////////////////////////////////////////////////////////////////////////////////////
void animal::fight()
{
  --hp;
  if(hp==0)
  {
    std::cout << "An animal was killed in a fight" << std::endl;
  }
}

